#include <ansi.h>
int update_condition(object me, int duration,int j)
{
     if(duration==1) 
     {  
     message_vision(HIW"$N身上白龙精元的效力已经消失!!\n"NOR,me);
     me->delete_temp("hardshell");
     return 0;
     }
     me->apply_condition("white_pill",duration-1);
     message_vision(HIW"白龙精元的效力慢慢在$N身上蔓延!!\n"NOR,me);
     if( duration < 1 )
     return 0;
     return 1;
}
